type Primitive = string | number | boolean | null | undefined

export type WidenLiterals<T> = T extends Primitive
  ? T extends string
    ? string
    : T extends number
    ? number
    : T
  : T extends readonly (infer Item)[]
  ? WidenLiterals<Item>[]
  : T extends object
  ? {
      [Key in keyof T]: WidenLiterals<T[Key]>
    }
  : T

export type UIText = WidenLiterals<typeof esText>
